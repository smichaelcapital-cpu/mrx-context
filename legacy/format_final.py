    text = re.sub(r'\s*\*REPORTER\s+CHECK\s+HERE\*', '', text, flags=re.IGNORECASE)
    # DEF-004: strip steno bracket artifact family
    text = re.sub(r'\s*\[\|+\]\s*', ' ', text)
