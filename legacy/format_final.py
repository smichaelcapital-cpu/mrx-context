_REVIEW_TAG_NAMES = (
    '[REVIEW:', '[AUDIO:', '[AGENT:', '[CONFIRMED:', '[NOTE:',
    '[CHANGED:', '[SUGGEST:', '[FLAG:', '[CORRECTED:',
)

def _strip_review_tags_safe(text):
    """
    Depth-aware tag stripper — handles single and double-bracket forms.

    Why this exists:
      Regex [^\\]]*] is greedy across newlines: when a REVIEW tag contains a nested
      bracket (e.g. [REVIEW: note "[REVIEW: nested]"...]) and all clean tags between
      it and the next ] have been removed by pass 1, pass 2 eats everything up to a
      ] hundreds of lines away.  BUG: ate 6,082 chars (130 lines) on Brandl 2026-04-13.

    Handles both forms:
      [REVIEW: content ]    — single bracket (standard)
      [[REVIEW: content ]]  — double bracket (AI agent extended notes)

    DEF-012 root cause: the old code checked text[i:i+8] == '[REVIEW:' only.
      For [[REVIEW: content ]], the outer [ was appended as a literal, then
      [REVIEW:...] was consumed, leaving outer [ + trailing ] = [].
      Fix: also detect [[ followed by a tag name and consume from the outer bracket.
    """
    out = []
    i = 0
    n = len(text)
    while i < n:
        # Check for double-bracket form: [[TAGNAME:
        # Use ]] delimiter scan rather than bracket depth-counting.
        # Reason: tag content often contains steno artifact brackets ('[ [|] drill') that
        # add unmatched depth — depth counter never reaches 0, eating the rest of the file.
        # The ]] delimiter is specific to double-bracket forms and is safe to scan for.
        matched_double = False
        if text[i] == '[' and i + 1 < n and text[i+1] == '[':
            for tag in _REVIEW_TAG_NAMES:
                if text[i+1:i+1+len(tag)] == tag:
                    # Scan forward for the ]] closing delimiter
                    close_pos = text.find(']]', i + 2)
                    if close_pos != -1:
                        i = close_pos + 2   # skip past ]]
                    else:
                        i += 2              # no ]] found — skip just [[
                    matched_double = True
                    break
        if matched_double:
            continue

        # Check for single-bracket form: [TAGNAME:
        matched_single = False
        for tag in _REVIEW_TAG_NAMES:
            if text[i:i+len(tag)] == tag:
                depth = 0
                while i < n:
                    if text[i] == '[':
                        depth += 1
                    elif text[i] == ']':
                        depth -= 1
                        if depth == 0:
                            i += 1
                            break
                    i += 1
                matched_single = True
                break
        if matched_single:
            continue

        out.append(text[i])
        i += 1
    return ''.join(out)


def inject_anchors(text):
    """
    Option B: Replace each [REVIEW:...] tag with a short anchor {R:N} where N is
    the correction_log index of that LOW/N/A item.  The anchor is tiny enough to
    survive block-joining, line-wrapping, and pagination unchanged.  After
    paginating we scan all_pages for {R:N} to get the exact p.XX l.YY for every
    flagged item, then strip anchors before writing the final file.

    Matching strategy: content-based.  For each LOW/N/A item we extract the
    [REVIEW:...] tag from its corrected field and find that exact tag in the
    source text, replacing it with {R:idx}.  This is correct even when the
    number of [REVIEW] tags in the text exceeds the number of LOW/N/A items
    (e.g. because HIGH/MEDIUM corrections also embedded [REVIEW] sub-notes).
    """
    log_path = 'correction_log.json'
    if not os.path.exists(log_path):
        return strip_review_tags(text), {}

    with open(log_path, encoding='utf-8') as f:
        data = json.load(f)

    corrections = data.get('corrections', [])
    anchor_map = {}  # correction_log_index -> anchor string

    for idx, item in enumerate(corrections):
        if item.get('confidence') not in ('LOW', 'N/A'):
            continue
        corrected = item.get('corrected', '')
        m = re.search(r'\[REVIEW:[^\]]*\]', corrected)
        if not m:
            continue
        review_tag = m.group(0)
        anchor = f'{{R:{idx}}}'

        if review_tag in text:
            pos = text.find(review_tag)
            # Guard: skip if the match is inside a [[REVIEW:...]] double-bracket form.
            # text[pos-1]=='[' means the single-bracket [REVIEW:...] is the inner tag of
            # [[REVIEW: ... [REVIEW: ...] ... ]] — replacing it here leaves outer_[{R:N}
            # which strip_anchors can only remove if the anchor is immediately followed
            # by ']'. When the outer [[REVIEW:...]] has more content after this inner tag,
            # the pattern []{R:N}] doesn't match and the outer [ + ]] become orphans.
            # Fix: skip injection and let _strip_review_tags_safe handle the whole outer form.
            if pos > 0 and text[pos - 1] == '[':
                pass  # skip — _strip_review_tags_safe will strip the [[REVIEW:...]] form
            else:
                text = text.replace(review_tag, anchor, 1)
                anchor_map[idx] = anchor
        else:
            # Partial match: try progressively shorter prefixes.
            # GUARD: skip if prefix match lands inside [[REVIEW:...]] double-bracket form.
            # If text[pos-1] == '[', we're matching the inner [REVIEW: of a [[REVIEW:...]] form.
            # A non-depth-aware find(']') then only finds the first ] in content, leaving:
            #   outer_[{R:N} + remaining content + ]] → orphaned ]] not caught by strip_anchors.
            # Solution: skip prefix match when preceded by '['; let _strip_review_tags_safe handle
            # the full [[REVIEW:...]] form as a unit.
            matched = False
            for prefix_len in (80, 60, 40, 20):
                if prefix_len >= len(review_tag):
                    continue
                prefix = review_tag[:prefix_len]
                pos = text.find(prefix)
                if pos != -1:
                    # Skip if this match is inside a [[REVIEW:...]] double-bracket form
                    if pos > 0 and text[pos - 1] == '[':
                        break  # don't try shorter prefixes — they'll hit the same spot
                    end = text.find(']', pos)
                    if end != -1:
                        text = text[:pos] + anchor + text[end + 1:]
                        anchor_map[idx] = anchor
                        matched = True
                        break

    # Strip any [REVIEW] tags that didn't match a LOW/N/A item, and all [FLAG] tags
    # Single-pass safe stripper — depth-aware, handles nested brackets correctly.
    # [REVIEW:] and [FLAG:] tags are ENGINE-GENERATED — defined in MASTER_DEPOSITION_ENGINE_v4.2.md
    # These are NOT from MB, the CR, or CaseCATalyst. They are our internal AI uncertainty flags.
    # Format: [REVIEW: explanation — reporter confirm] (verify-agent style)
    #      or [REVIEW: explanation] (standard inline style)
    # BUG HISTORY: .*? with re.DOTALL was eating 65,760 chars (34% of Easley) — fixed 2026-03-30
    # BUG HISTORY: [^\[]*? pass1 matched ACROSS nested tags — eating 3,294 lines — fixed 2026-04-13
    #   Root: [^\[]*? excludes [ but not ], so "[REVIEW: inner]" tail + outer-tag tail = one match
    #   Creates dangling [REVIEW: opener; safe stripper then ate from line 839 to 4133.
    #   Fix: eliminate regex passes entirely, use _strip_review_tags_safe() for all cases.
    # _strip_review_tags_safe handles both [REVIEW:...] and [[REVIEW:...]] natively.
    # DO NOT normalize [[ → [ here — that converts [[REVIEW:...]] into [REVIEW:...]]
    # (single bracket open, double bracket close), and the single-bracket stripper
    # stops at the first ], leaving a trailing ] in the output (root cause of DEF-012a).
    text = _strip_review_tags_safe(text)
    # Strip ~~REVIEW:...~~ system error tags (new format from ai_engine.py)
    # DEF-012/013: ~~REVIEW:...~~ system error tags — use [\s\S]*? so multiline tags are caught
    text = re.sub(r'\s*~~[\s\S]*?~~\s*', ' ', text)
    text = re.sub(r'\s*\[FLAG:[^\]]*\]', '', text)
    # DEF-011: strip *REPORTER CHECK HERE* — use \s+ so steno line-wrap (*REPORTER CHECK\nHERE*) is caught
    text = re.sub(r'\s*\*REPORTER\s+CHECK\s+HERE\*', '', text, flags=re.IGNORECASE)
    # Strip [CORRECTED:] audit-trail tags — engine-generated, not for final output.
    # The corrected text is already in place; the tag is an annotation only.
    text = re.sub(r'\s*\[CORRECTED:[^\]]*\]', '', text)
    # DEF-004: strip steno bracket artifact family — untranslated strokes that
    # never appear in legitimate testimony. Order matters: multi-char variants first.
    text = re.sub(r'\s*\[\|+\]\s*', ' ', text)    # [|]  [||]  [||||]
    text = re.sub(r"\s*\['\]\s*", ' ', text)       # [']
    text = re.sub(r'\s*\[\s*\|\s*\]\s*', ' ', text)  # [ | ] spaced variant
    text = re.sub(r'\s*\[\s*\]\s*', ' ', text)    # [] empty
    text = re.sub(r'  +', ' ', text)
    return text, anchor_map


def strip_anchors(pages):
    """Remove {R:N} anchors from all page lines after location capture."""
    cleaned = []
    for page_lines in pages:
        page = []
        for line in page_lines:
            # DEF-004/DEF-012: double-bracket [[REVIEW:]] forms leave []{R:N}] after anchor injection.
            # Strip []{R:N}] wrapper — the outer [] came from the double-bracket form, not testimony.
            line = re.sub(r'\[\s*\{R:\d+\}\s*\]', '', line)
            line = re.sub(r'\{R:\d+\}', '', line)
            page.append(line.rstrip())
        cleaned.append(page)
    return cleaned


